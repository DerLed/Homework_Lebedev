CREATE TABLE public.loan (
	loan_id int4 NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	amount numeric NOT NULL,
	rate numeric NOT NULL,
	loan_period int4 NOT NULL,
	start_date date NOT NULL,
	CONSTRAINT loan_pk null
);


